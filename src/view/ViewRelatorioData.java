    private void jbRelatorioClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRelatorioClientesActionPerformed
        String datai = JfDataInicial.getText();
        String dataf = JfDataFinal.getText();
        final ViewAguarde carregando = new ViewAguarde();
        carregando.setVisible(true);
        Thread t = new Thread() {
            public void run() {
                try {
                    controllerVendas.gerarRelatorioVendaData(datai, dataf);
                    carregando.dispose();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao gerar o relatorio! /n", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        };
        t.start();

    }//GEN-LAST:event_jbRelatorioClientesActionPerformed

